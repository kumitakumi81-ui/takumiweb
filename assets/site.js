function currentLanguageKey() {
  return document.documentElement.lang === "zh-Hans" ? "zh" : "en";
}

function formatYen(value) {
  if (currentLanguageKey() === "zh") {
    return `约${Math.round(value / 10000).toLocaleString("zh-CN")}万日元`;
  }
  const millions = Math.round(value / 1000000);
  return `¥${millions}M`;
}

function selectedEstimateType() {
  return document.querySelector("input[name='estimateType']:checked")?.value || "new";
}

function selectedText(element) {
  return element?.options[element.selectedIndex]?.textContent.trim() || "";
}

function estimateData() {
  if (!estimateArea || !estimateGrade || !estimatePerformance || !estimateTimeline) return null;
  const area = Number(estimateArea.value);
  const type = selectedEstimateType();
  const baseRates = { new: 240000, renovation: 80000, commercial: 130000 };
  const fixedCosts = { new: 2800000, renovation: 800000, commercial: 1000000 };
  const gradeFactors = { minimal: 0.78, standard: 0.92, premium: 1.08 };
  const performanceFactors = { basic: 0.9, comfort: 1, zeh: 1.12 };
  const midpoint = (area * baseRates[type] * gradeFactors[estimateGrade.value] * performanceFactors[estimatePerformance.value]) + fixedCosts[type];
  const lower = midpoint * 0.82;
  const upper = midpoint * 0.98;
  const typeLabel = document.querySelector("input[name='estimateType']:checked")?.closest(".option-card")?.querySelector("span")?.textContent.trim() || "";
  const gradeLabel = selectedText(estimateGrade);
  const performanceLabel = selectedText(estimatePerformance);
  const timelineLabel = selectedText(estimateTimeline);
  const lang = currentLanguageKey();
  const unit = dictionary[lang].estimateAreaUnit;
  return {
    area,
    type,
    range: `${formatYen(lower)} - ${formatYen(upper)}`,
    summary: `${typeLabel} / ${area} ${unit} / ${gradeLabel} / ${performanceLabel} / ${timelineLabel}`
  };
}

function updateEstimate() {
  const data = estimateData();
  if (!data || !estimateRange || !estimateSummary) return;
  const lang = currentLanguageKey();
  estimateAreaValue.textContent = `${data.area} ${dictionary[lang].estimateAreaUnit}`;
  estimateRange.textContent = data.range;
  estimateSummary.textContent = data.summary;
  if (estimateHidden) estimateHidden.value = `${dictionary[lang].estimateMessageIntro}: ${data.range} | ${data.summary}`;
  if (projectSelect) {
    const indexByType = { new: 0, renovation: 1, commercial: 2 };
    projectSelect.selectedIndex = indexByType[data.type] ?? 0;
  }
}

function estimateMessage() {
  const data = estimateData();
  if (!data) return "";
  const lang = currentLanguageKey();
  return `${dictionary[lang].estimateMessageIntro}\n${dictionary[lang].estimateResultLabel}: ${data.range}\n${data.summary}`;
}

