function setLanguage(lang) {
  const selected = dictionary[lang] ? lang : "en";
  document.documentElement.lang = selected === "zh" ? "zh-Hans" : "en";
  document.querySelectorAll("[data-copy]").forEach((node) => {
    const value = dictionary[selected][node.dataset.copy];
    if (value) node.textContent = value;
  });
  document.querySelectorAll("[data-placeholder]").forEach((node) => {
    const value = dictionary[selected][node.dataset.placeholder];
    if (value) node.setAttribute("placeholder", value);
  });
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.setAttribute("aria-pressed", String(button.dataset.lang === selected));
  });
  document.title = pageTitles[selected][page] || pageTitles[selected].home;
  const description = document.querySelector("meta[name='description']");
  if (description) {
    description.setAttribute("content", pageDescriptions[selected][page] || pageDescriptions[selected].home);
  }
  const ogTitle = document.querySelector("meta[property='og:title']");
  if (ogTitle) ogTitle.setAttribute("content", document.title);
  const ogDescription = document.querySelector("meta[property='og:description']");
  if (ogDescription && description) ogDescription.setAttribute("content", description.getAttribute("content"));
  const twitterTitle = document.querySelector("meta[name='twitter:title']");
  if (twitterTitle) twitterTitle.setAttribute("content", document.title);
  const twitterDescription = document.querySelector("meta[name='twitter:description']");
  if (twitterDescription && description) twitterDescription.setAttribute("content", description.getAttribute("content"));
  updateEstimate();
  try {
    localStorage.setItem("takumiLang", selected);
  } catch (error) {
  }
}

function getRequestedLanguage() {
  const params = new URLSearchParams(window.location.search);
  const lang = params.get("lang");
  if (lang === "zh" || lang === "zh-Hans" || lang === "cn") return "zh";
  if (lang === "en") return "en";
  return "";
}

function getDefaultLanguage() {
  const lang = document.body.dataset.defaultLang;
  return dictionary[lang] ? lang : "";
}

function getStoredLanguage() {
  try {
    const lang = localStorage.getItem("takumiLang");
    return dictionary[lang] ? lang : "";
  } catch (error) {
    return "";
  }
}

function getInitialLanguage() {
  return getRequestedLanguage() || getDefaultLanguage() || getStoredLanguage() || "en";
}

